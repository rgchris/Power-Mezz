			parse/all text [
				any [
					mk1: to #"&" mk2: (insert/part tail output mk1 mk2)
					#"&" [
						copy txt name #";" (append output entity-to-char txt)
						|
						"#x" copy txt some hexdigit #";" (append output hex-to-utf8char txt)
						|
						#"#" copy txt some digit #";" (append output dec-to-utf8char txt)
						|
						none (append output #"&")
